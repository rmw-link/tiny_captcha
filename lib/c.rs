unsafe fn blur(im: *mut u8) {
  let mut i: *mut u8 = im;
  let mut x: i32;
  let mut y: i32 = 0;
  while y < 68_i32 {
    x = 0_i32;
    while x < 198_i32 {
      let c11: u32 = *i as u32;
      let c12: u32 = *i.offset(1) as u32;
      let c21: u32 = *i.offset(200) as u32;
      let c22: u32 = *i.offset(201) as u32;
      let fresh1 = i;
      i = i.offset(1);
      *fresh1 = c11
        .wrapping_add(c12)
        .wrapping_add(c21)
        .wrapping_add(c22)
        .wrapping_div(4_i32 as u32) as u8;
      x += 1
    }
    y += 1
  }
}
unsafe fn filter(im: *mut u8) {
  let mut om: [u8; IMG_SIZE] = [255; IMG_SIZE];
  let mut i: *mut u8 = im;
  let mut o: *mut u8 = om.as_mut_ptr();
  let mut x: i32;
  let mut y: i32 = 0;
  while y < 70_i32 {
    x = 4_i32;
    while x < 200_i32 - 4_i32 {
      if (*i.offset(0) > 0xf0 && *i.offset(1) < 0xf0)
        || ((*i.offset(0)) < 0xf0 && *i.offset(1) > 0xf0)
      {
        *o.offset(0) = 0;
        *o.offset(1) = 0;
      }
      i = i.offset(1);
      o = o.offset(1);
      x += 1
    }
    y += 1
  }
  std::ptr::copy(&om as _, im, IMG_SIZE);
}
